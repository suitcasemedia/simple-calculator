<?php namespace App; 

class calculator{



	/**********************************************************************************************************
	Add method test
	/*********************************************************************************************************/
	
	public function add( $a , $b){
	
	return $a + $b;

	}


	/**********************************************************************************************************
	Subtract method test
	/*********************************************************************************************************/
	

	public function subtract( $a , $b)
	{
	return $a - $b;
	}


	/**********************************************************************************************************
	Multiply method test
	/*********************************************************************************************************/
	

	public function multiply( $a , $b)
	{
	return $a * $b;
	}


	/**********************************************************************************************************
	Divide method test
	/*********************************************************************************************************/
	

	public function divide( $a , $b)
	{
	return $a / $b;
	}


}
